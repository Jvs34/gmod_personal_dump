	function ENT:HandleFirstPersonView( ply , pos , ang , fov , nearz , farz )
		if self:IsCarriedBy( ply ) and ply:GetViewEntity() == self then
			
			local pos , ang = LocalToWorld( Vector( 4 , 0 , 0 ) , Angle( 180 , 90 , -270 ) , self:GetPos() , self:GetAngles() )
			
			fpcam.origin = pos
			fpcam.angles = ang
			fpcam.fov = fov
			fpcam.znear = nearz
			fpcam.zfar = farz
			fpcam.drawviewer = true
			
			return fpcam
		end
	end
	

	function ENT:HandleOwnerModel()
		
		if not self:IsCarried() or IsValid( self.HeadModel ) then
			return
		end
		
		
		self.HeadModel = ClientsideModel( self:GetControllingPlayer():GetModel() )
		self.HeadModel:SetNoDraw( true )
		self.HeadModel.RealOwner = self
		self.HeadModel.GetPlayerColor = function( ent )
			local owner = ent.RealOwner
			if IsValid( owner ) then
				if owner.GetControllingPlayer and IsValid( owner:GetControllingPlayer() ) then
					return owner:GetControllingPlayer():GetPlayerColor()
				end
			end
		end
		
		self.HeadModel:AddCallback( "BuildBonePositions" , function( ent , nbones )
			local owner = ent.RealOwner
			if IsValid( owner ) then
				if owner.HandleHeadBuildBonePositions then
					owner:HandleHeadBuildBonePositions( ent , nbones )
				end
			end
		end)
		
		self.HeadModel:AddCallback( "BuildFlexWeights" , function( ent , nflexes )
			local owner = ent.RealOwner
			if IsValid( owner ) then
				if owner.HandleHeadFlexes then
					return owner:HandleHeadFlexes( ent , nflexes )
				end
			end
		end)
	end
	
	function ENT:HandleHeadBuildBonePositions( headent , nbones )
		for i = 0 , nbones - 1 do
			local bm = headent:GetBoneMatrix( i )
			
			if bm then
				local bonename = headent:GetBoneName( i )
				bm:SetTranslation( self:GetPos() )
				bm:SetAngles( self:GetAngles() )
				
				if bonename:find( "Head" ) then
					bm:Translate( Vector( 0.5 , -0.5 , -0.25 ) )
				else
					bm:SetScale( Vector( 0.1 , 0.1 , 0.1 ) )
					--bm:Translate( Vector( 0 , 0 , 0 ) )
				end
				
				headent:SetBoneMatrix( i , bm )
			end
		end
	end
	
	local flexes = {}
	
	function ENT:HandleHeadFlexes( headent , nflexes )
		if not self:IsCarried() then
			return
		end
		
		headent:SetEyeTarget( headent:GetPos() )
		
		for i = 0 , nflexes -1 do
			flexes[i] = 0.5--self:GetControllingPlayer():GetFlexWeight( i + 1 )
		end
		
		return flexes
	end
	
